////////////////////////////////////////////////////////////////////////////////

function min(a, b) {
if(a < b) return a;
else return b
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {
if(number > 0){
  for(let i = number; i > -1; i -= 2) {
    if(i === 1) {
      return false;
    }
  }
} else {
  for(let i = number; i < 1; i += 2) {
    if(i=== -1) {
      return false
    }
  }
} return true;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, ch) {
let counted = 0;
for(let i = 0; i < string.length; i++) {
  if(string[i] === ch){
    counted += 1;
  }
} return counted;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let counted = 0;
for(let i = 0; i < string.length; i++) {
  if(string[i] === 'B') {
    counted += 1;
  }
} return counted;
}

